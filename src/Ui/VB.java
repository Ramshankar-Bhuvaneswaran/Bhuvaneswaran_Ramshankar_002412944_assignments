    private void BtnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex=jTable1.getSelectedRow();
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Please select row");
            return;
        }
        DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        Hw s= (Hw) model.getValueAt(selectedRowIndex, 0);
        BankName.setText(String.valueOf(s.getBankName()));
        SSN.setText(String.valueOf(s.getSSN()));
        TaxId.setText(String.valueOf(s.getTaxId()));
        Branch.setText(String.valueOf(s.getBranch()));
        AccountType.setText(String.valueOf(s.getAccountType()));

    }//GEN-LAST:event_BtnViewActionPerformed

    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        // TODO add your handling code here:
         int selectedRowIndex=jTable1.getSelectedRow();
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Please select row");
            return;
        }
        DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        Hw s= (Hw) model.getValueAt(selectedRowIndex, 0);
        
        bankaccount.deleteB(s);
        
        JOptionPane.showMessageDialog(this,"Row Deleted");
        populateTable();
    }//GEN-LAST:event_BtnDeleteActionPerformed


