    private void populateTable() {
        DefaultTableModel model=  (DefaultTableModel) jTable1.getModel();
         model.setRowCount(0);
         
         for (Hw vs : bankaccount.getB()){
             Object[] row= new Object[5];
             row[0]=vs.getBankName();
             row[1]=vs.getBranch();
             row[2]=vs.getSSN();
             row[3]=vs.getTaxId();
             row[4]=vs.getAccountType();
             
             
             model.addRow(row);
         }
    }
