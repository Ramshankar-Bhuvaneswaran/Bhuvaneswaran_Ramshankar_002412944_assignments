    private void BtnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex=jTable1.getSelectedRow();
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Please select row");
            return;
        }
        DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        allPerson s= (allPerson) model.getValueAt(selectedRowIndex, 0);
        FName.setText(String.valueOf(s.getFirstName()));
        Age.setText(String.valueOf(s.getAge()));
        BG.setText(String.valueOf(s.getBloodGroup()));
        LName.setText(String.valueOf(s.getLastName()));
        Sex.setText(String.valueOf(s.getSex()));

    }//GEN-LAST:event_BtnViewActionPerformed

    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        // TODO add your handling code here:
         int selectedRowIndex=jTable1.getSelectedRow();
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Please select row");
            return;
        }
        DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        allPerson s= (allPerson) model.getValueAt(selectedRowIndex, 0);
        
        person.deleteP(s);
        
        JOptionPane.showMessageDialog(this,"Row Deleted");
        populateTable();
    }//GEN-LAST:event_BtnDeleteActionPerformed


