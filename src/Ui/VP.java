    private void populateTable() {
        DefaultTableModel model=  (DefaultTableModel) jTable1.getModel();
         model.setRowCount(0);
         
         for (allPerson vs : person.getP()){
             Object[] row= new Object[5];
             row[0]=vs.getFirstName();
             row[1]=vs.getLastName();
             row[2]=vs.getAge();
             row[3]=vs.getSex();
             row[4]=vs.getBloodGroup();
             
             
             model.addRow(row);
    }
}
